package com.vichen.image;

/**
 * @author chenyu
 * @Date: 2018/10/22 18:20
 */
public class ImageParams {
  private String image;
  private String url;
  private boolean highPrecisionStr;

  public String getImage() {
    return image;
  }

  public void setImage(String image) {
    this.image = image;
  }

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public boolean isHighPrecisionStr() {
    return highPrecisionStr;
  }

  public void setHighPrecisionStr(boolean highPrecisionStr) {
    this.highPrecisionStr = highPrecisionStr;
  }
}
